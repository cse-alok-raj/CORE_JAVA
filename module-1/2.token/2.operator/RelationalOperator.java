class RelationalOperator {
	public static void main(String[] args) {
		int i = 10;
		System.out.println("Value of i is: " + i);

		int j = 4;
		System.out.println("Value of j is: " + j);

		boolean c = i > j;
		System.out.println("Is i > j: " + c);

		boolean x = i >= j;
		System.out.println("Is i >= j: " + x);

		boolean d = i < j;
		System.out.println("Is i < j: " + d);

		boolean y = i <= j;
		System.out.println("Is i <= j: " + y);

		boolean e = i == j;
		System.out.println("Is i == j: " + e);

		boolean f = i != j;
		System.out.println("Is i != j: " + f);
	}
}