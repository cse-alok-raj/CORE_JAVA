class BasicCalculator {
	public static String addition(int i, int j) {
		return "Addition of " + i + " and " + j + " is: " +(i+j);
	}

	public static String addition(int i, int j, int k) {
		return "Addition of " + i + ", " + j + " and " + k + " is: " +(i+j+k);
	}

	public static String addition(double i, double j) {
		return "Addition of " + i + " and " + j + " is: " +(i+j);
	}

	public static String addition(double i, double j, double k) {
		return "Addition of " + i + ", " + j + " and " + k + " is: " +(i+j+k);
	}
}